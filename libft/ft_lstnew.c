#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*tab;

	tab = malloc(sizeof(t_list));
	if (!tab)
		return (0);
	tab->content = content;
	tab->next = NULL;
	return (tab);
}
